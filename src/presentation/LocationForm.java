    // N3mro Tableau
void loadLocations() {
        model.setRowCount(0);
        for (entities.Location l : locationService.findAll()) {
            model.addRow(new Object[]{
                l.getId_location(),
                l.getBien().getType() + " (" + l.getBien().getVille() + ")",
                l.getDateDebut(),
                l.getDateFin(),
                l.getLoyer()
            });
        }
    }

    void loadBiens() {
        comboBien.removeAllItems();
        for (entities.Bien b : bienService.findAll()) {
            comboBien.addItem(b);
        }
    }

    void vider() {
        txtDateDebut.setText("");
        txtDateFin.setText("");
        txtLoyer.setText("");
        idSelect = 0;
    }
    
    private void tableLocationMouseClicked(java.awt.event.MouseEvent evt) {                                           
        int row = tableLocation.getSelectedRow();
        if (row != -1) {
            idSelect = Integer.parseInt(model.getValueAt(row, 0).toString());
            txtDateDebut.setText(model.getValueAt(row, 2).toString());
            Object dFin = model.getValueAt(row, 3);
            if(dFin != null) txtDateFin.setText(dFin.toString());
            else txtDateFin.setText("");
            txtLoyer.setText(model.getValueAt(row, 4).toString());
        }
    }
