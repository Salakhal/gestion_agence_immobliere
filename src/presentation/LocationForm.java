    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(idSelect != 0){
             int rep = javax.swing.JOptionPane.showConfirmDialog(this, "Voulez-vous modifier ?");
             if(rep == 0){
                 try {
                    java.sql.Date dDebut = java.sql.Date.valueOf(txtDateDebut.getText());
                    java.sql.Date dFin = null;
                    if(!txtDateFin.getText().isEmpty()){
                        dFin = java.sql.Date.valueOf(txtDateFin.getText());
                    }
                    double loyer = Double.parseDouble(txtLoyer.getText());
                    entities.Bien bienSelect = (entities.Bien) comboBien.getSelectedItem();
                    
                    entities.Location l = new entities.Location(idSelect, dDebut, dFin, loyer, bienSelect);
                    locationService.update(l);
                    loadLocations();
                    vider();
                 } catch (Exception e) {
                      javax.swing.JOptionPane.showMessageDialog(this, "Erreur Modif: " + e.getMessage());
                 }
             }
         } else {
             javax.swing.JOptionPane.showMessageDialog(this, "Sélectionnez une ligne !");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

