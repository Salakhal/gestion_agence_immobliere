    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       if(idSelect != 0){
             int rep = javax.swing.JOptionPane.showConfirmDialog(this, "Voulez-vous supprimer ?");
             if(rep == 0){
                 entities.Location l = new entities.Location();
                 l.setId_location(idSelect);
                 locationService.delete(l);
                 loadLocations();
                 vider();
             }
        } else {
             javax.swing.JOptionPane.showMessageDialog(this, "Sélectionnez une ligne !");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

 
