    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
   try {
            // Code dyal AJOUTER (Création)
            java.sql.Date dDebut = java.sql.Date.valueOf(txtDateDebut.getText());
            java.sql.Date dFin = null;
            if(!txtDateFin.getText().isEmpty()) dFin = java.sql.Date.valueOf(txtDateFin.getText());
            
            double loyer = Double.parseDouble(txtLoyer.getText());
            entities.Bien bienSelect = (entities.Bien) comboBien.getSelectedItem();

            // Création
            entities.Location loc = new entities.Location(dDebut, dFin, loyer, bienSelect);
            if(locationService.create(loc)){
                javax.swing.JOptionPane.showMessageDialog(this, "Location Ajoutée !");
                loadLocations();
                vider();
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erreur (Format Date YYYY-MM-DD): " + e.getMessage());
        }                             
    }//GEN-LAST:event_jButton1ActionPerformed
