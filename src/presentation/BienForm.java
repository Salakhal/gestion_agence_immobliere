   // Méthode loadBiens JDIDA (Avec Recherche)
    private void loadBiens() {
        model.setRowCount(0); // Nkhwiw tableau
        
        // 1. Njibo l'kalima li kteb l'utilisateur (w nrdoha sghira bach tkon search sahla)
        String motCle = "";
       

        for (entities.Bien b : bienService.findAll()) {
            // 2. Ncheckiw: Wach smiyat l'Ville fiha had l'motCle?
            if (b.getVille().toLowerCase().contains(motCle)) {
                
                String nomProp = "Aucun";
                if (b.getProprietaire() != null) {
                    nomProp = b.getProprietaire().getNom();
                }
                
                // 3. Ila kant, nzidoha f tableau
                model.addRow(new Object[]{
                    b.getId_bien(),
                    b.getType(),
                    b.getVille(),
                    b.getSurface(),
                    b.getPrixMensuel(),
                    nomProp
                });
            }
        }
    }
