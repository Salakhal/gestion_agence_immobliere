    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
      try {
            // Récupération des nombres
            double surf = Double.parseDouble(txtSurface.getText());
            double prix = Double.parseDouble(txtPrix.getText());
            
            // Récupération du propriétaire choisi
            Proprietaire pSelect = (Proprietaire) comboProp.getSelectedItem();

            if(bienService.create(new Bien(txtType.getText(), txtVille.getText(), surf, prix, pSelect))){
                JOptionPane.showMessageDialog(this, "Bien Ajouté !");
                loadBiens();
                vider();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erreur : Surface et Prix doivent être des nombres !");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage());
        }  
    }//GEN-LAST:event_jButton1ActionPerformed
// 3. SUPPRIMER (jButton2)
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (idSelect != 0) {
            if (JOptionPane.showConfirmDialog(this, "Supprimer ce bien ?") == JOptionPane.YES_OPTION) {
                Bien b = new Bien();
                b.setId_bien(idSelect);
                bienService.delete(b);
                loadBiens();
                vider();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Sélectionnez une ligne !");
        }
    }//GEN-LAST:event_jButton2ActionPerformed
// 4. MODIFIER (jButton3)
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
       if (idSelect != 0) {
            if (JOptionPane.showConfirmDialog(this, "Modifier ce bien ?") == JOptionPane.YES_OPTION) {
                try {
                    double surf = Double.parseDouble(txtSurface.getText());
                    double prix = Double.parseDouble(txtPrix.getText());
                    Proprietaire pSelect = (Proprietaire) comboProp.getSelectedItem();

                    Bien b = new Bien(idSelect, txtType.getText(), txtVille.getText(), surf, prix, pSelect);
                    bienService.update(b);
                    loadBiens();
                    vider();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Vérifiez les données !");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Sélectionnez une ligne !");
        } 
    }//GEN-LAST:event_jButton3ActionPerformed

