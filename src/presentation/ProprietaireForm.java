// --- BOUTON AJOUTER (jButton1) ---
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // Verification que les champs ne sont pas vides
        if(txtNom.getText().isEmpty() || txtContact.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Remplissez les champs !");
            return;
        }

        Proprietaire p = new Proprietaire(txtNom.getText(), txtContact.getText(), txtAdresse.getText());

        if(propService.create(p)){
            JOptionPane.showMessageDialog(this, "Propriétaire Ajouté !");
            loadProprietaires();
            vider();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // 1. Nchoufo wach l'bouton aslan khdam
        System.out.println("--> Bouton Supprimer Clické !");
        
        // 2. Nchoufo wach l'ID tshad
        System.out.println("--> ID Actuel = " + idSelect);

        if (idSelect != 0) {
             if (JOptionPane.showConfirmDialog(this, "Supprimer ?") == JOptionPane.YES_OPTION) {
                 entities.Proprietaire p = new entities.Proprietaire();
                 p.setId_prop(idSelect);
                 
                 // 3. Nchoufo wach DAO 3ayetna lih
                 System.out.println("--> Appel DAO Delete...");
                 
                 if (propService.delete(p)) {
                     System.out.println("--> Suppression Réussie !");
                     loadProprietaires();
                     vider();
                 } else {
                     System.out.println("--> Echec Suppression (Problème DAO)");
                 }
             }
        } else {
             System.out.println("--> Erreur: ID = 0 (Aucune ligne sélectionnée)");
             JOptionPane.showMessageDialog(this, "Sélectionnez une ligne !");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(idSelect != 0){
             if(JOptionPane.showConfirmDialog(this, "Modifier ce propriétaire ?") == JOptionPane.YES_OPTION){
                 // Hna knakhdo l'info Jdida mn TextFields (txtNom...)
                 entities.Proprietaire p = new entities.Proprietaire(idSelect, txtNom.getText(), txtContact.getText(), txtAdresse.getText());
                 
                 if(propService.update(p)){
                     JOptionPane.showMessageDialog(this, "Modifié avec succès !");
                     loadProprietaires();
                     vider();
                 }
             }
        } else {
             JOptionPane.showMessageDialog(this, "Sélectionnez une ligne dans le tableau !");
        }
    }//GEN-LAST:event_jButton3ActionPerformed


