   // Méthode loadProprietaires (Avec Recherche)
    private void loadProprietaires() {
        model.setRowCount(0); // Nkhwiw tableau
        
        // Njibo l'mot clé (ila kano l'champs kaynin)
        String motCle = "";
        if(txtRecherche != null) {
            motCle = txtRecherche.getText().toLowerCase();
        }

        for (entities.Proprietaire p : propService.findAll()) {
            // Ncheckiw wach Nom aw Contact fihom dakchi li mktoub
            if (p.getNom().toLowerCase().contains(motCle) || p.getContact().toLowerCase().contains(motCle)) {
                
                model.addRow(new Object[]{
                    p.getId_prop(), 
                    p.getNom(), 
                    p.getContact(), 
                    p.getAdresse()
                });
            }
        }
    }
    // Vider les champs
  void vider() {
        txtNom.setText(""); 
        txtContact.setText(""); 
        txtAdresse.setText(""); 
        idSelect = 0;
    }
