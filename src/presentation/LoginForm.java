    private void lblForgotMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblForgotMouseClicked
        // TODO add your handling code here:
        String loginSaisi = JOptionPane.showInputDialog(this, "Entrez votre Login pour recevoir le mot de passe :");
        
        if(loginSaisi != null && !loginSaisi.isEmpty()) {
            try {
                Connection cn = Connexion.getConnection();
                String sql = "SELECT email, password FROM users WHERE login = ?";
                PreparedStatement ps = cn.prepareStatement(sql);
                ps.setString(1, loginSaisi);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    String emailDest = rs.getString("email");
                    String passRecup = rs.getString("password");
                    
                    if(emailDest != null && !emailDest.isEmpty()){
                        JOptionPane.showMessageDialog(this, "Envoi de l'email en cours...");
                        
                        // --- ENVOI EMAIL ---
                        // (Ila ma 3ndkch EmailSender, mssa7 had l'ligne w dir msg 3adi)
                        EmailSender.sendPassword(emailDest, passRecup);
                        
                        JOptionPane.showMessageDialog(this, "Mot de passe envoyé à : " + emailDest);
                    } else {
                        JOptionPane.showMessageDialog(this, "Aucun email associé à ce compte.");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Ce login n'existe pas.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erreur : " + e.getMessage());
            }
        }
    }//GEN-LAST:event_lblForgotMouseClicked

