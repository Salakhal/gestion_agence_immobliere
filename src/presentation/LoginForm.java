    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        // TODO add your handling code here:
        String log = txtNom.getText();
        String pass = new String(txtPass.getPassword());

        if (log.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !");
            return;
        }

        try {
            // 1. Connexion Base
            Connection cn = Connexion.getConnection();
            
            // 2. Verification
            String sql = "SELECT * FROM users WHERE login = ? AND password = ?";
            PreparedStatement ps = cn.prepareStatement(sql);
            ps.setString(1, log);
            ps.setString(2, pass);
            
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                // Login S7i7 -> Hello MainFrame
                new MainFrame().setVisible(true);
                this.dispose(); // Ssed Login
            } else {
                // Ghalat
                JOptionPane.showMessageDialog(this, "Login ou Mot de passe incorrect !", "Erreur", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur Connexion: " + e.getMessage());
        }
    }//GEN-LAST:event_btnConnectActionPerformed
